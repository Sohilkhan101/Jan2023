package Lecture1;

public class IncDecDemo {
	public static void main(String[] args) {
		int a=12;
		int b=10;
		System.out.println(a++);
		System.out.println(++a);
		System.out.println(a);
		System.out.println(++a);
		
		
		System.out.println(b--);
		System.out.println(b--);
		System.out.println(b++);
		System.out.println(--b);
	}

}
